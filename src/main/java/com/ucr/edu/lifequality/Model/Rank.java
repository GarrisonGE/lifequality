package com.ucr.edu.lifequality.Model;

public class Rank {
    private Location ul;
    private Location lr;
    private float rank;

    public Location getUl() {
        return ul;
    }

    public void setUl(Location ul) {
        this.ul = ul;
    }

    public Location getLr() {
        return lr;
    }

    public void setLr(Location lr) {
        this.lr = lr;
    }

    public float getRank() {
        return rank;
    }

    public void setRank(float rank) {
        this.rank = rank;
    }
}
